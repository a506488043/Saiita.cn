package Saiita.cn.entity;

public class Bugs {
  String number;
  String trace_debug;
  String status;
  String Level;
  String theme;
  String author;
  String assigned_to;
  String percent_completion;
  String start_date;
  String target_version;
public String getNumber() {
	return number;
}
public void setNumber(String number) {
	this.number = number;
}
public String getTrace_debug() {
	return trace_debug;
}
public void setTrace_debug(String trace_debug) {
	this.trace_debug = trace_debug;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}
public String getLevel() {
	return Level;
}
public void setLevel(String level) {
	Level = level;
}
public String getTheme() {
	return theme;
}
public void setTheme(String theme) {
	this.theme = theme;
}
public String getAuthor() {
	return author;
}
public void setAuthor(String author) {
	this.author = author;
}
public String getAssigned_to() {
	return assigned_to;
}
public void setAssigned_to(String assigned_to) {
	this.assigned_to = assigned_to;
}
public String getPercent_completion() {
	return percent_completion;
}
public void setPercent_completion(String percent_completion) {
	this.percent_completion = percent_completion;
}
public String getStart_date() {
	return start_date;
}
public void setStart_date(String start_date) {
	this.start_date = start_date;
}
public String getTarget_version() {
	return target_version;
}
public void setTarget_version(String target_version) {
	this.target_version = target_version;
}
  
}
